<b>Noto Rewards Card System</b><br />
Noto Restaurant uses a Bonus Reward card system.<br />
-The 10% Bonus Cards can be obtained from the restaurant manager.<br />
-The card it attached to a person but can be transferred to be used by a third party.<br />
-The card is good for one year after its registration.<br />
-If the card is lost it can be replaced.<br />
-You can only apply bonus points to the card if you have the card on hand.<br /> 
<div id = "noto_club_steps">
    <div class='block'>    
        <div>How to get the card</div>
        <div>
            1)	The guest fills out a “Guest Form” and gives it to the restaurant manager<br /> 
            2)	By signing the form the guest agrees to have his personal information added to a restaurant mailing list for purposes of advertising by the restaurant management<br />
            3)	Registration of the new card takes up to 24 hours <br />
            4)	The guest can use the card upon his next visit to the restaurant<br />
            5)	The card’s expiration date is exactly 1 year after its registration (any unused bonus points with disappear at this time)<br />
       </div>
    </div>
    <div class='block'>    
        <div>How the card works</div>
        <div>
            1)	When the guest asks for his check, he must show the waitstaff his #NOTOcard.<br />
            2)	After the check has been settled, bonus reward points are added to the #NOTOcard.<br />
            3)	The guest can transfer his card to 3 other persons.<br />
            4)	The Rewards Card cannot be used for delivery service. 
        </div>
   </div>
    <div class='block'>    
        <div>The Rewards</div>
        <div>
            -	10% of the check total will be added to your Reward Card<br />
            -	You will receive a monthly newsletter from “Noto Food and People”<br />
            -	Priority access to making a reservation during our special events.<br />
            -	Discounts on restaurant promotions<br />
            -	A bottle of wine for your birthday (if you have 4 or more guests celebrating and can show ID)<br />
            -	Access to events from partner establishments<br />
        </div>
   </div>
   <div class='block'>    
        <div>Guest Perks</div>
        <div>
            -	To get a print-out of the current balance<br />
            -	To get a replacement card without losing the current reward points<br />
            -	The ability to apply the card rewards to pay for a check without having a minimum balance.<br /> 
        </div>
   </div>
