<b>El sistema de puntos de NOTO.</b> <br />
El restaurante NOTO tiene un sistema de tarjetas de puntos.<br />
<br />
-Las tarjetas del 10% se entregan por el encargado del restaurante.<br />
-La tarjeta va inscrita a un nombre pero el cliente puede darla a terceras personas.<br />
-La tarjeta tiene un año de validez a partir del día de su entrega.<br />
-En caso de pérdida de la tarjeta se puede hacer un duplicado.<br />
-Los puntos pueden ser cargados solo si la tarjeta se presenta a la hora de pagar la cuenta.<br />
<div id = "noto_club_steps">
    <div class='block'>    
        <div>El procedimiento de entrega de la tarjeta</div>
        <div>
            1. El cliente rellena el cuestionario “cuestionario del cliente” y la entrega al encargado del restaurante.<br />
            2. El cliente al rellenar el cuestionario da su permiso para el tratamiento y el uso de sus datos personales por el encargado del restaurante con fines publicitarios.<br />
            3. La activación de la tarjeta puede tardar 24 horas (pero nunca puede superar este plazo).<br />
            4. El cliente puede utilizar la tarjeta durante la próxima visita.<br />
            5. La validez de la tarjeta es de un año después de su activación (si quedan puntos sin utilizar, desaparecen).<br />
       </div>
    </div>
    <div class='block'>    
        <div>El procedimiento de funcionamiento del sistema de puntos</div>
        <div>
            1. El cliente a la hora de pedir la cuenta presenta su tarjeta  #NOTOcard al camarero.<br />
            2. Una vez pagada la cuenta a la tarjeta #NOTOcard se cargan los puntos.<br />
            3. El cliente puede pasar su tarjeta a 3 personas.<br />
            4. La tarjeta #NOTOcard no puede ser aplicada para los pedidos con entrega a domicilio y para llevar.
        </div>
   </div>
    <div class='block'>    
        <div>Privilegios que tienen los clientes</div>
        <div>
            - El 10% de la cuenta se convierte en puntos y se carga a la tarjeta;<br />
            - Los clientes reciben las noticias mensuales del restaurante «NOTO Food & people»;<br />
            - Los clientes tienen privilegios a la hora de reservar la mesa para eventos especiales;<br />
            - Precios especiales para las promociones del restaurante.<br />
            - Una botella de vino de regalo a la hora de celebrar un cumpleaños si la cantidad de invitados es de 4 personas mínimo. Hace falta presentar el DNI.<br />
            - La tarjeta da derecho a participar en las promociones de los socios del restaurante.<br />
        </div>
   </div>
   <div class='block'>    
        <div>Opciones para los clientes</div>
        <div>
            - Bajo la petición del cliente, se puede imprimir el extracto de la tarjeta.<br />
            - En caso de pérdida de la tarjeta se puede hacer un duplicado con todos los puntos.<br />
            - Se puede pagar la cuenta con los puntos sin restricciones.<br /> 
        </div>
   </div>
