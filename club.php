<b>Бонусная система ресторана NOTO food &amp; people</b> <br />
В ресторане NOTO действует система бонусных карт.<br />
<br />
- Накопительные карты номиналом 10% выдаются менеджером ресторана<br />
- Карта именная, но допускается передача третьим лицам<br />
- Если карта не используется в течение года, бонусы аннулируются<br />
- В случае потери карта восстанавливается, а накопленные бонусы сохраняются<br />
- Бонусы по счету начисляются только при предъявлении карты<br />
<div id = "noto_club_steps">
    <div class='block'>    
        <div>Механика получения карты</div>
        <div>
            1) Гость заполняет анкету и передает ее менеджеру ресторана.<br />
            2) Заполняя анкету, гость дает свое согласие на обработку и использование его персональных данных менеджментом ресторана для рекламных рассылок.<br />
            3) Регистрация карты в системе может занять не более 24 часов.<br />
            4) Гость может воспользоваться картой при следующем посещении ресторана.<br /> 
            5) Срок действия накопленных бонусов составляет 1 год с момента активации карты (если в течение этого времени бонусы не используются, то их аннулируют)
        </div>
   </div>
    <div class='block'>    
        <div>Принцип работы бонусной системы</div>
        <div>
            1) Гость при посещении ресторана просит счет у официанта и предъявляет #NOTOcard.<br />
            2) После оплаты счета происходит зачисление бонусов на карту #NOTOcard.<br />
            3) Гость может передавать карту третьим лицам.<br />
            4) Действие бонусной карты не распространяется на услугу доставки еды и блюда на вынос.<br />
        </div>
   </div>
    <div class='block'>    
        <div>Привилегии, которые мы предоставляем гостю</div>
        <div>
            - Перечисление 10 % от общей суммы оплаченного счета на #NOTOcard.<br />
            - Участие в ежемесячной новостной рассылке ресторана NOTO food &amp; people.<br />
            - Привилегированное бронирование на специальные мероприятия.<br />
            - Специальные цены на промоакции ресторана.<br />
            - Бутылка вина на день рождения – при условии празднования в компании от 4 человек и предъявлении паспорта.<br />
            - Участие в партнерских программах.
        </div>
   </div>
   <div class='block'>    
        <div>Опции для гостей</div>
        <div>
            - Распечатка баланса карты по запросу гостя.<br />
            - Восстановление карты с сохранением бонусов.<br />
            - Частичная или полная оплата счета бонусами без ограничений минимального накопления.
        </div>
   </div>
